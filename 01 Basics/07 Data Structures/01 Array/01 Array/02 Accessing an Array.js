/// Array
// An Array holds an ordered sequence of items.

// Creating an Array
let myArray = [26, "six", 3.14, true];
console.log(myArray);  // [ 26, 'six', 3.14, true ]

// Accessing the Existing Elements in an Array with Indexing concept.
// Index starts from 0
console.log(myArray[0]);  // 26
console.log(myArray[1]);  // six

// Accessing the No-Existing Elements in an Array
// we will get undefined when using index at out of range.
console.log(myArray[4]);  // undefined
