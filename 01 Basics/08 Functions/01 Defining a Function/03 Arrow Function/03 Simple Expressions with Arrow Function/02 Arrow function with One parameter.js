
/// Functions

// Arrow function

/* -----> Arrow function with one parameter <----- */

let greet1 = (number) => number * number;
let output1 = greet1(5);
console.log(output1);  // 25


// we can remove round brackets, if function has single argument.
let greet2 = number => number * number;
let output2 = greet2(10);
console.log(output2);  // 100
