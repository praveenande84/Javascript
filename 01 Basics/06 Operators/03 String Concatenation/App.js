
/// operators

/*
    String Concatenation
    + (when, string+number or string+string)
 */


// String + String
let firstName = "Ande";
let lastName = "Praveen";

let fullName = firstName + lastName;
console.log(fullName);  // AndePraveen
console.log(typeof(fullName));  // string

// String + Number
let myAge = 27;
let output = fullName + myAge;
console.log(output);  // AndePraveen27
