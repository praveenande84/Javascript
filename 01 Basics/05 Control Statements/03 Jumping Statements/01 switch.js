/*

 switch statement
 A switch statement is a conditional statement like if-else statement used in decision making.
 
 */

let day = 4;

switch (day){
    case 0:
        console.log("Sunday");
        break;

    case 1:
        console.log("Monday");
        break;

    case 2:
        console.log("Tuesday");
        break;

    case 3:
        console.log("Wednesday");
        break;

    case 4:
        console.log("Thursday");  // Thursday
        break;

    case 5:
        console.log("Friday");
        break;

    case 6:
        console.log("Saturday");
        break;

    default:
        console.log("Please Enter Valid Number");
        break;
}

// output => Thursday


