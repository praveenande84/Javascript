/// style
// The style property is used to get or set a specific style of an HTML Element using different CSS properties.

// Element selection through id
let headingElement = document.getElementById("heading-element");

// add CSS styles to the html Element
// manipulating the style of html Element
// use camelCase for style properties
headingElement.style.backgroundColor = "orange";

// camelCase
// normal property => background-color
// camelCase property => backgroundColor