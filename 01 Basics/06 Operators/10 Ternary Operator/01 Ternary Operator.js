// Ternary Operator

/*

 A Ternary Operator can be used to replace if-else statements in some situations.
 condition ? expressionIfTrue : expressionIfFalse

 */

// let variable = condition ? if-block : else-block;


let speed = 70;

let message = (speed >= 70) ? "Too Fast!" : "Ok";

console.log(message);  // Too Fast!

