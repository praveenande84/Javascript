/// Array
// An Array holds an ordered sequence of items.

// Creating an Array
let myArray = [26, "six", 3.14, true];
console.log(myArray);  // [ 26, 'six', 3.14, true ]

// Modifying an Array
// we can use Indexing technique to Modifying an Array Item
myArray[1] = "Praveen"
console.log(myArray);  // [ 26, 'Praveen', 3.14, true ]