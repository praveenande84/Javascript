/// Data Structures

// Object
// Modifying Object property with Bracket Notation

// creating an Object
let person = {
    name: "Praveen",
    age: 26,
    city: "Hyderabad",
    'my choice': "Software"
}

console.log(person.name);  // Praveen

// Modifying Object property with Bracket Notation
person["name"] = "Swathi";
console.log(person.name);  // Swathi

