
// Javascript Values

// check datatype
// typeof() operator is used to find the type of value.

let myValue = 100;  // integer value
let PiValue = 3.14;  // float value
let isDone = true;  // boolean value

console.log(typeof(myValue));  // number
console.log(typeof(PiValue));  // number
console.log(typeof(isDone));  // boolean


let myName = "Ande Praveen";  // double quotations
let city = 'Hyderabad';  // single quotations
let Job = `Software Engineer`;  // back tick

console.log(typeof(myName));  // string
console.log(typeof(city));  // string
console.log(typeof(Job));  // string

// undefined type
let age;
console.log(typeof(age));  // undefined



