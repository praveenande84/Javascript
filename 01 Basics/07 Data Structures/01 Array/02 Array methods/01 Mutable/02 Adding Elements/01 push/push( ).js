
/// Array Methods

// Adding Elements

/*

 push() method
 The push() method adds new items to the end of the array.
 myArray.push(lastItem);

 */

let myArray = [1, 2, 3, 4, 5];
console.log(myArray); // [ 1, 2, 3, 4, 5 ]

let lastItem = "six";
myArray.push(lastItem);

console.log(myArray);  // [ 1, 2, 3, 4, 5, 'six' ]