
// Normal Array Destructuring

let [a, b, c] = [1, 2, 3];


console.log(a);  // 1
console.log(b);  // 2
console.log(c);  // 3


// Array Destructuring with rest parameter

let [A, B, C, ...rest] = [1, 2, 3, 4, 5];

console.log(A);  // 1
console.log(B);  // 2
console.log(C);  // 3
console.log(rest);  // [ 4, 5 ]





