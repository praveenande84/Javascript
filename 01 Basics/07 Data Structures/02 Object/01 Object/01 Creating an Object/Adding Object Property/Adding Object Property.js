/// Data Structure

// Object
// adding Object property

// creating an Object
let person = {
    name: "praveen",
    age: 26,
    city: "Hyderabad",
}

console.log(person);  // { name: 'praveen', age: 26, city: 'Hyderabad' }

// adding Object property with Dot Notation
person.gender = "Male";

// adding Object Property with Bracket Notation
person["role"] = "Developer";

console.log(person);  // {name: 'praveen', age: 26, city: 'Hyderabad', gender: 'Male', role: 'Developer'}


