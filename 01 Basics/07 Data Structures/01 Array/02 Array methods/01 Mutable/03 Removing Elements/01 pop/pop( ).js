
/// Array Methods

// Removing Elements

/*

 pop() method
 The pop() method removes the last item of an array and returns that item.
 myArray.pop();

 */

let myArray = [1, 2, 3, 4, 5];
console.log(myArray); // [ 1, 2, 3, 4, 5 ]

let lastItem = myArray.pop();
console.log(lastItem);  // 5

console.log(myArray);  // [ 1, 2, 3, 4 ]

